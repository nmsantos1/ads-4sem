    private void novo() {
        
        try{
            int opcao = JOptionPane.showConfirmDialog(null, 
                    "Deseja inserir novo Funcionario?", 
                    "MENSAGEM NOVO FUNCIONARIO", JOptionPane.YES_OPTION);
            if (opcao == JOptionPane.YES_OPTION){
                limparDados();
                txtRm.requestFocus();
            }
        } catch (Exception e){
            System.out.println(e.getMessage());
        }
    }

    private void limparDados() {

        txtRm.setText("");
        txtNome.setText("");
        txtEndereco.setText("");
        txtTelefone.setText("");
    }

    private void cadastrar() {

        try{
            FuncionarioCTR funcionarioctr = new FuncionarioCTR();
            if (funcionarioctr.cadastrarFuncionario(Integer.parseInt(txtRm.getText()),
                    txtNome.getText(), txtEndereco.getText(), txtTelefone.getText())){
                JOptionPane.showMessageDialog(null, "Funcion√°rio cadastrado com sucesso!", 
                        "Mensagem de grava√ß√£o", JOptionPane.INFORMATION_MESSAGE);
                txtRm.requestFocus();
            } else{
                JOptionPane.showMessageDialog(null, "Erro, problema ao cadastrar Funcionario", 
                        "Mensagem de grava√ß√£o", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
